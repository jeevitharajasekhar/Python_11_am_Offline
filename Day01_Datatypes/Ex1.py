#Dynamic typing and dynamic changing
a = 10
b = 20
print(a) #10
print(b) #20

# a is a variable
# = is a operator
# 10 and 20 is value/constants/literals
a = 30
print(a) #30

x = 10 #10 is whole number
print(type(x)) #<class 'int'> #

y = 20.0 #20.0 is decimal number
print(type(y)) #<class 'float'>

z = "Python"
print(type(z)) #<class 'str'>
